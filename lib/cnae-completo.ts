/**
 * Busca a descrição de um CNAE pelo código
 * @param codigo - Código do CNAE (com ou sem formatação: "6203100" ou "6203-1/00")
 * @returns Descrição do CNAE ou mensagem padrão
 * @example
 * getCNAEDescription('6203100') // "DESENVOLVIMENTO E LICENCIAMENTO DE PROGRAMAS..."
 * getCNAEDescription('6203-1/00') // "DESENVOLVIMENTO E LICENCIAMENTO DE PROGRAMAS..."
 */
export function getCNAEDescription(codigo: string): string {
  if (!codigo) return 'Código não informado';
  
  // Remove formatação e espaços
  const cleanCode = codigo.replace(/[^0-9]/g, '');
  
  // Busca direta na tabela completa
  if (CNAE_COMPLETO[cleanCode]) {
    return CNAE_COMPLETO[cleanCode];
  }
  
  // Fallback: Busca por prefixo (primeiros 6 dígitos)
  // Útil quando o código vem sem o dígito verificador
  const shortCode = cleanCode.substring(0, 6);
  const foundCompleto = Object.keys(CNAE_COMPLETO).find(key => key.startsWith(shortCode));
  
  if (foundCompleto) {
    return CNAE_COMPLETO[foundCompleto];
  }
  
  return 'Descrição não disponível';
}

/**
 * Busca CNAEs por descrição (pesquisa textual)
 * @param termo - Termo de busca (ex: "software", "restaurante", "construção")
 * @param limite - Número máximo de resultados (padrão: 10)
 * @returns Array de CNAEs encontrados com código e descrição
 * @example
 * buscarCNAE('software', 5) // [{ codigo: '6201501', descricao: '...' }, ...]
 */
export function buscarCNAE(termo: string, limite: number = 10): Array<{ codigo: string; descricao: string }> {
  const termoLower = termo.toLowerCase();
  const resultados: Array<{ codigo: string; descricao: string }> = [];
  
  for (const [codigo, descricao] of Object.entries(CNAE_COMPLETO)) {
    if (descricao.toLowerCase().includes(termoLower)) {
      resultados.push({ codigo, descricao });
      if (resultados.length >= limite) break;
    }
  }
  
  return resultados;
}

/**
 * Formata o código CNAE no padrão oficial: 0000-0/00
 * @param codigo - Código do CNAE (apenas números)
 * @returns Código formatado
 * @example
 * formatCNAECode('6203100') // "6203-1/00"
 */
export function formatCNAECode(codigo: string): string {
  if (!codigo) return '';
  
  const cleanCode = codigo.replace(/[^0-9]/g, '');
  
  if (cleanCode.length === 7) {
    return `${cleanCode.substring(0, 4)}-${cleanCode.substring(4, 5)}/${cleanCode.substring(5, 7)}`;
  }
  
  return codigo;
}

/**
 * Verifica se um código CNAE é válido
 * @param codigo - Código do CNAE
 * @returns true se o código existe na base
 */
export function isCNAEValido(codigo: string): boolean {
  if (!codigo) return false;
  const cleanCode = codigo.replace(/[^0-9]/g, '');
  return cleanCode in CNAE_COMPLETO;
}

/**
 * Retorna todos os CNAEs como array
 * @returns Array de objetos com código e descrição
 */
export function getAllCNAEs(): Array<{ codigo: string; descricao: string }> {
  return Object.entries(CNAE_COMPLETO).map(([codigo, descricao]) => ({
    codigo,
    descricao
  }));
}

/**
 * Busca CNAEs por seção (letra inicial)
 * @param secao - Letra da seção (A-U)
 * @returns Array de CNAEs da seção
 * @example
 * buscarPorSecao('J') // Retorna todos CNAEs de Informação e Comunicação
 */
export function buscarPorSecao(secao: string): Array<{ codigo: string; descricao: string }> {
  // Mapeamento de primeira letra do código para seção
  const secaoMap: Record<string, string[]> = {
    'A': ['0'],
    'B': ['0'],
    'C': ['1', '2', '3'],
    'D': ['3'],
    'E': ['3'],
    'F': ['4'],
    'G': ['4'],
    'H': ['4', '5'],
    'I': ['5'],
    'J': ['5', '6'],
    'K': ['6'],
    'L': ['6'],
    'M': ['6', '7'],
    'N': ['7', '8'],
    'O': ['8'],
    'P': ['8'],
    'Q': ['8'],
    'R': ['9'],
    'S': ['9'],
    'T': ['9'],
    'U': ['9']
  };
  
  const prefixos = secaoMap[secao.toUpperCase()];
  if (!prefixos) return [];
  
  return Object.entries(CNAE_COMPLETO)
    .filter(([codigo]) => prefixos.some(p => codigo.startsWith(p)))
    .map(([codigo, descricao]) => ({ codigo, descricao }));
}

